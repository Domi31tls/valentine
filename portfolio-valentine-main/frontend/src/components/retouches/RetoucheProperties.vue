  <script setup lang="ts">
  import { ref, computed, watch } from 'vue';
  import type { Retouche, Media } from '../../../../shared/types';
  
  interface Props {
    retouche: Partial<Retouche>;
    beforeImage?: Media;
    afterImage?: Media;
  }
  
  const props = defineProps<Props>();
  const emit = defineEmits<{
    'update:retouche': [retouche: Partial<Retouche>];
    'change': [];
  }>();
  
  // √âtat local
  const localRetouche = ref<Partial<Retouche>>({ ...props.retouche });
  const showSeo = ref(false);
  const newKeyword = ref('');
  
  // Computed
  const keywords = computed({
    get: () => {
      const seoKeywords = localRetouche.value.seo?.keywords;
      if (Array.isArray(seoKeywords)) {
        return seoKeywords;
      }
      return [];
    },
    set: (value: string[]) => {
      if (!localRetouche.value.seo) {
        localRetouche.value.seo = {};
      }
      localRetouche.value.seo.keywords = value;
      emitChange();
    }
  });
  
  const keywordsCount = computed(() => keywords.value.length);
  const descriptionLength = computed(() => (localRetouche.value.description || '').length);
  const seoTitleLength = computed(() => (localRetouche.value.seo?.title || '').length);
  const seoDescriptionLength = computed(() => (localRetouche.value.seo?.description || '').length);
  
  // Auto-g√©n√©ration SEO pour retouches
  const autoGeneratedSEO = computed(() => {
    const title = localRetouche.value.title || '';
    const description = localRetouche.value.description || '';
    
    return {
      title: title ? `${title} - Before & After Retouching | Valentine Arnaly` : '',
      description: description ? 
        description.length > 160 ? 
          description.substring(0, 157) + '...' : 
          description : 
        'Professional photo retouching showcase - see the dramatic before and after transformation.',
    };
  });
  
  // SEO Preview
  const displaySeoTitle = computed(() => {
    const title = localRetouche.value.seo?.title || autoGeneratedSEO.value.title;
    return title.length > 60 ? title.substring(0, 57) + '...' : title;
  });
  
  const displaySeoDescription = computed(() => {
    const description = localRetouche.value.seo?.description || autoGeneratedSEO.value.description;
    return description.length > 160 ? description.substring(0, 157) + '...' : description;
  });
  
  // Images compatibility
  const dimensionsMatch = computed(() => {
    if (!props.beforeImage || !props.afterImage) return true;
    return props.beforeImage.width === props.afterImage.width && 
           props.beforeImage.height === props.afterImage.height;
  });
  
  const aspectRatioMatch = computed(() => {
    if (!props.beforeImage || !props.afterImage) return true;
    const beforeRatio = props.beforeImage.width / props.beforeImage.height;
    const afterRatio = props.afterImage.width / props.afterImage.height;
    return Math.abs(beforeRatio - afterRatio) < 0.01;
  });
  
  // Watchers
  watch(() => props.retouche, (newRetouche) => {
    localRetouche.value = { ...newRetouche };
  }, { deep: true });
  
  // M√©thodes
  const emitChange = () => {
    emit('update:retouche', localRetouche.value);
    emit('change');
  };
  
  const autoGenerateSEO = () => {
    if (!localRetouche.value.seo) {
      localRetouche.value.seo = {};
    }
    
    if (localRetouche.value.title) {
      localRetouche.value.seo.title = autoGeneratedSEO.value.title;
    }
    
    if (localRetouche.value.description) {
      localRetouche.value.seo.description = autoGeneratedSEO.value.description;
    }
    
    // Auto-generate retouching keywords
    const retouchingKeywords = extractRetouchingKeywords(
      `${localRetouche.value.title || ''} ${localRetouche.value.description || ''}`
    );
    
    if (retouchingKeywords.length > 0) {
      localRetouche.value.seo.keywords = [
        ...new Set([...keywords.value, ...retouchingKeywords])
      ].slice(0, 10);
    }
    
    emitChange();
  };
  
  const extractRetouchingKeywords = (text: string): string[] => {
    const retouchingTerms = [
      'retouche', 'retouching', 'photoshop', 'before', 'after', 'transformation',
      'editing', 'enhancement', 'correction', 'restoration', 'beauty', 'portrait',
      'photography', 'professional', 'digital', 'image', 'photo'
    ];
    
    const words = text
      .toLowerCase()
      .replace(/[^\w\s]/g, '')
      .split(/\s+/)
      .filter(word => word.length > 3);
    
    return retouchingTerms
      .filter(term => text.toLowerCase().includes(term))
      .slice(0, 5);
  };
  
  const addKeyword = () => {
    const keyword = newKeyword.value.trim().replace(/,+$/, '');
    if (keyword && !keywords.value.includes(keyword) && keywords.value.length < 10) {
      keywords.value = [...keywords.value, keyword];
      newKeyword.value = '';
    }
  };
  
  const removeKeyword = (index: number) => {
    keywords.value = keywords.value.filter((_, i) => i !== index);
  };
  
  const generateSlug = (title: string): string => {
    return title
      .toLowerCase()
      .normalize('NFD')
      .replace(/[\u0300-\u036f]/g, '')
      .replace(/[^a-z0-9\s-]/g, '')
      .replace(/\s+/g, '-')
      .replace(/-+/g, '-')
      .replace(/^-|-$/g, '');
  };
  
  const formatDate = (dateString?: string) => {
    if (!dateString) return 'N/A';
    
    const date = new Date(dateString);
    return date.toLocaleDateString('fr-FR', {
      day: 'numeric',
      month: 'short',
      year: 'numeric',
      hour: '2-digit',
      minute: '2-digit'
    });
  };
  
  const formatFileSize = (bytes: number): string => {
    if (bytes < 1024) return `${bytes}B`;
    if (bytes < 1024 * 1024) return `${(bytes / 1024).toFixed(1)}KB`;
    return `${(bytes / 1024 / 1024).toFixed(1)}MB`;
  };
  
  const getFileFormat = (mimeType: string): string => {
    switch (mimeType) {
      case 'image/jpeg': return 'JPEG';
      case 'image/png': return 'PNG';
      case 'image/webp': return 'WebP';
      case 'image/gif': return 'GIF';
      default: return mimeType.replace('image/', '').toUpperCase();
    }
  };
  </script>
  
